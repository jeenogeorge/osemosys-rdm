"""Pre-process MUIO/OSeMOSYS data file to reduce matrix generation time

This script pre-processes an OSeMOSYS input data file generated by MUIO (v5.4+)
by adding lines that list commodity-technology-mode combinations that data is
provided for, along with calculated CapitalRecoveryFactor and PvAnnuity parameters.

Pre-processing consists of the following steps:

1. Parsing the bracket-format parameters from the MUIO-generated data file:
   ``OutputActivityRatio``, ``InputActivityRatio``, ``EmissionActivityRatio``,
   ``EmissionToActivityChangeRatio``, ``TechnologyToStorage``,
   ``TechnologyFromStorage``, ``InputToNewCapacityRatio``,
   ``InputToTotalCapacityRatio``, ``OperationalLife``, ``DiscountRateIdv``,
   and ``DiscountRate``.
2. Calculating ``CapitalRecoveryFactor`` and ``PvAnnuity`` for each technology.
3. Adding preprocessed sets: ``MODExTECHNOLOGYperFUELout``,
   ``MODExTECHNOLOGYperFUELin``, ``MODExTECHNOLOGYperEMISSION``,
   ``MODExTECHNOLOGYperEMISSIONChange``, ``MODExTECHNOLOGYperSTORAGEto``,
   ``MODExTECHNOLOGYperSTORAGEfrom``, ``INPUTxNEWxCAPACITYperFUEL``,
   ``INPUTxTOTALxCAPACITYperFUEL``, ``MODEperTECHNOLOGY``, ``INPUTxFUEL``.

In order to start a model run with a pre-processed data file, the following sets
need to be introduced to its associated OSeMOSYS model file::

    set MODEperTECHNOLOGY{TECHNOLOGY} within MODE_OF_OPERATION;
    set MODExTECHNOLOGYperFUELout{COMMODITY} within MODE_OF_OPERATION cross TECHNOLOGY;
    set MODExTECHNOLOGYperFUELin{COMMODITY} within MODE_OF_OPERATION cross TECHNOLOGY;
    set MODExTECHNOLOGYperSTORAGEto{STORAGE} within MODE_OF_OPERATION cross TECHNOLOGY;
    set MODExTECHNOLOGYperSTORAGEfrom{STORAGE} within MODE_OF_OPERATION cross TECHNOLOGY;
    set MODExTECHNOLOGYperEMISSION{EMISSION} within MODE_OF_OPERATION cross TECHNOLOGY;
    set MODExTECHNOLOGYperEMISSIONChange{EMISSION} within MODE_OF_OPERATION cross TECHNOLOGY;
    set INPUTxNEWxCAPACITYperFUEL{COMMODITY} within TECHNOLOGY;
    set INPUTxTOTALxCAPACITYperFUEL{COMMODITY} within TECHNOLOGY;
    set INPUTxFUEL within COMMODITY;

Usage::

    python preprocess_data_muio.py <infile> <outfile>

"""

import sys
import traceback
from collections import defaultdict


def parse_set_line(line):
    """Extract set members from a single-line set definition.

    Handles: ``set NAME := VAL1 VAL2 ... ;``
    """
    after_eq = line.split(":=")[1].strip()
    after_eq = after_eq.rstrip(";").strip()
    if after_eq:
        return after_eq.split()
    return []


def main(data_infile, data_outfile):

    # =========================================================================
    # STEP 1 - Read lines, filtering out any previous preprocessing output
    # =========================================================================
    lines = []
    skip_param_block = False

    with open(data_infile, "r") as f1:
        for line in f1:
            # Skip previously appended preprocessed sets
            if line.startswith(
                ("set MODEper", "set MODEx", "set INPUTx", "end;")
            ):
                continue

            # Skip CapitalRecoveryFactor / PvAnnuity param blocks (header → ;)
            if line.startswith(("param CapitalRecoveryFactor", "param PvAnnuity")):
                skip_param_block = True
                continue
            if skip_param_block:
                if line.startswith(";"):
                    skip_param_block = False
                continue

            lines.append(line)

    # =========================================================================
    # STEP 2 - Parse sets from the data file
    # =========================================================================
    year_list = []
    fuel_list = []
    tech_list = []
    emi_list = []
    stg_list = []

    with open(data_infile, "r") as f:
        for line in f:
            if line.startswith("set YEAR "):
                year_list = parse_set_line(line)
            elif line.startswith("set COMMODITY "):
                fuel_list = parse_set_line(line)
            elif line.startswith("set FUEL "):
                fuel_list = parse_set_line(line)
            elif line.startswith("set TECHNOLOGY "):
                tech_list = parse_set_line(line)
            elif line.startswith("set EMISSION "):
                emi_list = parse_set_line(line)
            elif line.startswith("set STORAGE ") and not line.startswith(
                "set STORAGEINTRA"
            ):
                stg_list = parse_set_line(line)

    if not year_list:
        print("ERROR: Could not parse set YEAR from data file.")
        sys.exit(1)

    start_year = year_list[0]

    # =========================================================================
    # STEP 3 - Parse parameters (MUIO bracket format)
    # =========================================================================
    # Parameters that trigger parsing
    BRACKET_PARAMS = (
        "param OutputActivityRatio",
        "param InputActivityRatio",
        "param EmissionActivityRatio",
        "param EmissionToActivityChangeRatio",
        "param InputToNewCapacityRatio",
        "param InputToTotalCapacityRatio",
        "param TechnologyToStorage",
        "param TechnologyFromStorage",
    )
    TABLE_PARAMS = (
        "param OperationalLife",
        "param DiscountRateIdv",
    )
    SIMPLE_PARAMS = ("param DiscountRate",)

    # Params that use firstRow logic (tech/mode header before data rows)
    FIRST_ROW_PARAMS = (
        "param OperationalLife",
        "param DiscountRateIdv",
        "param TechnologyToStorage",
        "param TechnologyFromStorage",
    )

    all_trigger_params = BRACKET_PARAMS + TABLE_PARAMS + SIMPLE_PARAMS

    data_all = []
    input_fuel_list = []
    data = {}

    with open(data_infile, "r") as f:
        parsing = False
        param_current = None
        tech = None
        fuel_emi = None
        firstRow = False
        techs = []
        modes = []

        for raw_line in f:
            line = raw_line.rstrip().replace("\t", " ")

            if line.startswith(";"):
                parsing = False
                continue

            if parsing:
                # --- Bracket header: [REGION,TECH,FUEL_OR_STAR,*,*]: ---
                if line.startswith("["):
                    element = line.split(",")
                    tech = element[1]
                    fuel_emi = element[2] if len(element) > 2 else None

                # --- Years header line (e.g. "2020 2021 ... :=") ---
                elif line.startswith(start_year):
                    pass  # skip, we don't need per-year values

                # --- Data rows ---
                else:
                    values = line.rstrip().split(" ")[1:]

                    # DiscountRate: "REGION VALUE"
                    if param_current == "DiscountRate":
                        parts = line.split()
                        if len(parts) >= 2:
                            region = parts[0]
                            dr = parts[1]
                            data[param_current].append((region, dr))

                    # OperationalLife / DiscountRateIdv: table with tech header
                    elif param_current in ("OperationalLife", "DiscountRateIdv"):
                        if firstRow:
                            techs = line.rstrip(":= ;\n").split()
                            techs = [t.strip(":=") for t in techs if t.strip(":=")]
                            firstRow = False
                        else:
                            parts = line.split()
                            if len(parts) > 1:
                                region = parts[0]
                                vals = parts[1:]
                                for i, t in enumerate(techs):
                                    if i < len(vals):
                                        data[param_current].append((region, t, vals[i]))

                    # Activity ratios: mode is first field
                    elif param_current in (
                        "OutputActivityRatio",
                        "InputActivityRatio",
                        "EmissionActivityRatio",
                        "EmissionToActivityChangeRatio",
                    ):
                        mode = line.split(" ")[0]
                        if mode and tech and fuel_emi:
                            data[param_current].append((fuel_emi, tech, mode))
                            data_all.append((tech, mode))

                    # InputToNewCapacity / InputToTotalCapacity: fuel is first field
                    elif param_current in (
                        "InputToNewCapacityRatio",
                        "InputToTotalCapacityRatio",
                    ):
                        fuel = line.split(" ")[0]
                        if fuel and tech:
                            data[param_current].append((fuel, tech))
                            if fuel not in input_fuel_list:
                                input_fuel_list.append(fuel)

                    # TechnologyToStorage / TechnologyFromStorage
                    elif param_current in (
                        "TechnologyToStorage",
                        "TechnologyFromStorage",
                    ):
                        if firstRow:
                            modes = line.rstrip(":= ;\n").split()
                            modes = [m.strip(":=") for m in modes if m.strip(":=")]
                            firstRow = False
                        else:
                            parts = line.split()
                            if len(parts) > 1:
                                stg = parts[0]
                                stg_values = parts[1:]
                                for i, mode in enumerate(modes):
                                    if i < len(stg_values) and stg_values[i] != "0":
                                        data[param_current].append((stg, tech, mode))
                                        data_all.append((tech, mode))

            # --- Detect parameter headers ---
            if any(line.startswith(p) for p in all_trigger_params):
                param_current = line.split(" ")[1]
                data[param_current] = []
                parsing = True
                firstRow = any(line.startswith(p) for p in FIRST_ROW_PARAMS)

    # =========================================================================
    # STEP 4 - Deduplicate and build dictionaries
    # =========================================================================
    data_out = list(set(data.get("OutputActivityRatio", [])))
    data_inp = list(set(data.get("InputActivityRatio", [])))
    data_emi = list(set(data.get("EmissionActivityRatio", [])))
    data_emichange = list(set(data.get("EmissionToActivityChangeRatio", [])))
    data_tts = list(set(data.get("TechnologyToStorage", [])))
    data_tfs = list(set(data.get("TechnologyFromStorage", [])))
    data_itnc = list(set(data.get("InputToNewCapacityRatio", [])))
    data_ittc = list(set(data.get("InputToTotalCapacityRatio", [])))
    data_all = list(set(data_all))

    dict_out = defaultdict(list)
    dict_inp = defaultdict(list)
    dict_all = defaultdict(list)
    dict_emi = defaultdict(list)
    dict_emichange = defaultdict(list)
    dict_tts = defaultdict(list)
    dict_tfs = defaultdict(list)
    dict_itnc = defaultdict(list)
    dict_ittc = defaultdict(list)

    for fuel, tech, mode in data_out:
        dict_out[fuel].append((mode, tech))
    for fuel, tech, mode in data_inp:
        dict_inp[fuel].append((mode, tech))
    for emi, tech, mode in data_emi:
        dict_emi[emi].append((mode, tech))
    for emi, tech, mode in data_emichange:
        dict_emichange[emi].append((mode, tech))
    for stg, tech, mode in data_tts:
        dict_tts[stg].append((mode, tech))
    for stg, tech, mode in data_tfs:
        dict_tfs[stg].append((mode, tech))
    for fuel, tech in data_itnc:
        dict_itnc[fuel].append(tech)
    for fuel, tech in data_ittc:
        dict_ittc[fuel].append(tech)
    for tech, mode in data_all:
        if mode not in dict_all[tech]:
            dict_all[tech].append(mode)

    # =========================================================================
    # STEP 5 - Calculate CapitalRecoveryFactor and PvAnnuity
    # =========================================================================
    OL_data = data.get("OperationalLife", [])
    DRi_data = data.get("DiscountRateIdv", [])
    DR_data = data.get("DiscountRate", [])

    has_crf_data = OL_data and DRi_data and DR_data

    if has_crf_data:
        DR = float(DR_data[0][1])

        OL = {}
        DRi = {}
        for _r, t, ol in OL_data:
            OL[t] = int(ol)
        for _r, t, dri in DRi_data:
            DRi[t] = float(dri)

        techs_string = " ".join(tech_list) + " "

        CapitalRecoveryFactor = {}
        PvAnnuity = {}
        for tech in tech_list:
            if tech not in DRi or DRi[tech] == 0:
                CapitalRecoveryFactor[tech] = None
            else:
                CapitalRecoveryFactor[tech] = round(
                    (1 - pow((1 + DRi[tech]), -1))
                    / (1 - pow((1 + DRi[tech]), -OL[tech])),
                    4,
                )
            if tech not in OL or DR == 0:
                PvAnnuity[tech] = None
            else:
                PvAnnuity[tech] = round(
                    (1 - pow((1 + DR), -OL[tech])) * (1 + DR) / DR, 4
                )

        # Append CRF param to lines
        lines.append(
            "param CapitalRecoveryFactor default 0 : \n"
        )
        lines.append("{}:=\n".format(techs_string))
        rt = " ".join(str(CapitalRecoveryFactor[t]) for t in tech_list)
        lines.append("RE1 {} \n".format(rt))
        lines.append(";\n")

        # Append PvAnnuity param to lines
        lines.append("param PvAnnuity default 0 : \n")
        lines.append("{}:=\n".format(techs_string))
        rt = " ".join(str(PvAnnuity[t]) for t in tech_list)
        lines.append("RE1 {} \n".format(rt))
        lines.append(";\n")

    # =========================================================================
    # STEP 6 - Write output file
    # =========================================================================
    def file_output_function(d, set_list, set_name, extra_char, output_type=None):
        for each in set_list:
            if each in d:
                line = (
                    set_name + str(each) + "]:=" + str(d[each]) + extra_char
                )
                if set_list == tech_list:
                    line = (
                        line.replace(",", "")
                        .replace(":=[", ":= ")
                        .replace("]*", "")
                        .replace("'", "")
                    )
                elif output_type == "input":
                    line = (
                        line.replace(",", "")
                        .replace(":=[", ":= ")
                        .replace("']", "")
                        .replace("'", "")
                    )
                else:
                    line = (
                        line.replace("),", ")")
                        .replace("[(", " (")
                        .replace(")]", ")")
                        .replace("'", "")
                    )
            else:
                line = set_name + str(each) + "]:="

            file_out.write(line + ";\n")

    with open(data_outfile, "w") as file_out:
        file_out.writelines(lines)

        file_output_function(
            dict_out, fuel_list, "set MODExTECHNOLOGYperFUELout[", ""
        )
        file_output_function(
            dict_inp, fuel_list, "set MODExTECHNOLOGYperFUELin[", ""
        )

        file_output_function(
            dict_emi, emi_list, "set MODExTECHNOLOGYperEMISSION[", ""
        )
        file_output_function(
            dict_emichange,
            emi_list,
            "set MODExTECHNOLOGYperEMISSIONChange[",
            "",
        )

        file_output_function(
            dict_tts, stg_list, "set MODExTECHNOLOGYperSTORAGEto[", ""
        )
        file_output_function(
            dict_tfs, stg_list, "set MODExTECHNOLOGYperSTORAGEfrom[", ""
        )

        file_output_function(
            dict_itnc,
            input_fuel_list,
            "set INPUTxNEWxCAPACITYperFUEL[",
            "",
            output_type="input",
        )
        file_output_function(
            dict_ittc,
            input_fuel_list,
            "set INPUTxTOTALxCAPACITYperFUEL[",
            "",
            output_type="input",
        )

        file_output_function(
            dict_all, tech_list, "set MODEperTECHNOLOGY[", "*"
        )

        line = "set INPUTxFUEL:=" + ", ".join(input_fuel_list)
        file_out.write(line + ";\n")

        file_out.write("end;")


if __name__ == "__main__":
    if len(sys.argv) != 3:
        msg = "Usage: python {} <infile> <outfile>"
        print(msg.format(sys.argv[0]))
        sys.exit(1)

    data_infile = sys.argv[1]
    data_outfile = sys.argv[2]

    try:
        main(data_infile, data_outfile)
        print("Preprocessing complete: {} -> {}".format(data_infile, data_outfile))
    except Exception as err:
        print("Unexpected error: {}".format(err))
        traceback.print_exc()
        sys.exit(1)